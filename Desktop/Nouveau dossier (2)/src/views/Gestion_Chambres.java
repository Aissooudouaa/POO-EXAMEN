    private void header_button_deleteMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_header_button_deleteMousePressed
        if(jTable1.getSelectedRowCount() == 1)
        {
            String ref = jTable1.getValueAt(jTable1.getSelectedRows()[0], 0).toString();

            int dialogButton = jOptionPane1.YES_NO_OPTION;
            int dialogResult = jOptionPane1.showConfirmDialog (null, "Souhaitez-vous supprimer cet enregistrement?", "Warning", dialogButton);
            if(dialogResult == jOptionPane1.YES_OPTION){
                old_layout.db_.delete_chambre(Integer.parseInt(ref));
                this.refresh_table();
            }
        }
    }//GEN-LAST:event_header_button_deleteMousePressed

