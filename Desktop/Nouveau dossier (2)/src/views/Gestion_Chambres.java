    public void refresh_table()
    {
        Map<Integer, chambre> list_chambre = old_layout.db_.list_chambre();
        
        Object[][] data = new Object[list_chambre.size()][3];
        
        if (list_chambre.size() > 0)
        {
            int i = 0;
            for (int j = 0; j < list_chambre.size(); j++) {
                data[j][0] = list_chambre.get(j).getChambre_id();
                data[j][1] = list_chambre.get(j).getChambre_nom();
                data[j][2] = list_chambre.get(j).getChambre_nbr_personne();
            }
        }
        
        jTable1.setModel(new javax.swing.table.DefaultTableModel(
            data,
            new String [] {
                "ID", "Nom", "Nbr personne"
            }
        ){
            boolean[] canEdit = new boolean [] {
                false, false, false, false, false, false, false
            };

            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit [columnIndex];
            }
        });
    }

