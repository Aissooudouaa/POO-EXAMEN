    public static boolean isDateValid(String date)
    {
        String DATE_FORMAT = "yyyy-MM-dd";
        try {
            DateFormat df = new SimpleDateFormat(DATE_FORMAT);
            df.setLenient(false);
            df.parse(date);
            return true;
        } catch (ParseException e) {
            return false;
        }
    }
    
    private void Button_add_new_studentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_add_new_studentActionPerformed
        if( this.reservation_date.getText().equals("") )
        {
            this.error_message.setText("error...ecrire la date");
        }
        else if( !isDateValid(this.reservation_date.getText()) )
        {
            this.error_message.setText("error... la date ne sont pas a ce format yyyy-mm-dd");
        }
        else
        {
            Object unit_id_object = user_id.getSelectedItem();
            String user_id_ = ((ComboItem)unit_id_object).getValue();
            
            Object chambre_id_object = chambre_id.getSelectedItem();
            String chambre_id_ = ((ComboItem)chambre_id_object).getValue();
            
            if( this.mode_edit )
            {
                String old_reservation_date = element_array.getReservation_date();
                
                element_array.setUser_id( old_layout.db_.getUserByUserId(Integer.parseInt(user_id_)) );
                element_array.setChambre_id( old_layout.db_.getchambreBychambreId(Integer.parseInt(chambre_id_)) );
                element_array.setReservation_date( String.valueOf(this.reservation_date.getText()) );
                element_array.setReservation_etat( String.valueOf(this.reservation_etat.getSelectedItem()) );
                
                if (!old_reservation_date.equals(element_array.getReservation_date()) ) {
                    if ( !old_layout.db_.check_available_date(old_layout.db_.getchambreBychambreId(Integer.parseInt(chambre_id_)), 
                        this.reservation_date.getText()) ) {
                        this.error_message.setText("error... la date ne pas disponible");
                        
                        return;
                    }
                }
                
                old_layout.db_.edit_reservation(element_array);

                back_layout.refresh_table();

                this.setVisible(false);
            }
            else
            {
                reservation new_reservation = new reservation();
                new_reservation.setReservation_id(old_layout.db_.list_reservation().size() + 1);
                new_reservation.setUser_id( old_layout.db_.getUserByUserId(Integer.parseInt(user_id_)) );
                new_reservation.setChambre_id( old_layout.db_.getchambreBychambreId(Integer.parseInt(chambre_id_)) );
                new_reservation.setReservation_date( String.valueOf(this.reservation_date.getText()) );
                new_reservation.setReservation_etat( String.valueOf(this.reservation_etat.getSelectedItem()) );
                
                if ( !old_layout.db_.check_available_date(old_layout.db_.getchambreBychambreId(Integer.parseInt(chambre_id_)), 
                        this.reservation_date.getText()) ) {
                    this.error_message.setText("error... la date ne pas disponible");
                }
                else
                {
                    

                    old_layout.db_.ajouter_reservation(new_reservation);

                    back_layout.refresh_table();

                    this.setVisible(false);
                }
            }
        }
    }//GEN-LAST:event_Button_add_new_studentActionPerformed

