    public void refresh_chambres() {
        for (int i = 0; i < old_layout.db_.list_chambre().size(); i++) {
            String chambre_id = String.valueOf(old_layout.db_.list_chambre().get(i).getChambre_id());
            String chambre_full_name = String.valueOf(old_layout.db_.list_chambre().get(i).getChambre_nom());
            
            this.chambre_id.addItem(new ComboItem(chambre_full_name, chambre_id));
        }
    }
    
    public void refresh_users() {
        for (int i = 0; i < old_layout.db_.list_users().size(); i++) {
            String user_id = String.valueOf(old_layout.db_.list_users().get(i).getUser_id());
            String user_full_name = String.valueOf(old_layout.db_.list_users().get(i).getUser_nom()) + " " + 
                    String.valueOf(old_layout.db_.list_users().get(i).getUser_prenom());
            
            this.user_id.addItem(new ComboItem(user_full_name, user_id));
        }
        
        if (old_layout.user_array.getUser_type().equals("client")) {
            this.user_id.setVisible(false);
        }
    }
    
    public void refresh_etats() {
        this.reservation_etat.addItem("En-attend-Payement");
        this.reservation_etat.addItem("Payé");
    }

