    private void Button_add_new_studentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_add_new_studentActionPerformed
        if( this.user_nom.getText().equals("") )
        {
            this.error_message.setText("error...ecrire le nom de l'utilisateur");
        }
        else if( this.user_prenom.getText().equals("") )
        {
            this.error_message.setText("error...ecrire le prenom de l'utilisateur");
        }
        else
        {
            
            
            if( this.mode_edit )
            {
                element_array.setUser_login(this.user_login.getText());
                element_array.setUser_nom(this.user_nom.getText());
                element_array.setUser_prenom(this.user_prenom.getText());
                element_array.setUser_password(this.user_pass.getText());
                element_array.setUser_type(String.valueOf(this.user_type.getSelectedItem()));
                
                old_layout.db_.edit_user(element_array);
            }
            else
            {
                user new_user = new user();
                new_user.setUser_id(old_layout.db_.list_users().size() + 1);
                new_user.setUser_login(this.user_login.getText());
                new_user.setUser_nom(this.user_nom.getText());
                new_user.setUser_prenom(this.user_prenom.getText());
                new_user.setUser_password(this.user_pass.getText());
                new_user.setUser_type(String.valueOf(this.user_type.getSelectedItem()));
                
                old_layout.db_.ajouter_user(new_user);
            }
            
            back_layout.refresh_table();

            this.setVisible(false);
        }
    }//GEN-LAST:event_Button_add_new_studentActionPerformed

